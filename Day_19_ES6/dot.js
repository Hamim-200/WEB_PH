const student = {
    name: "Hamim",
    age: 22,
    "student id": "CSE-101"
};

console.log(student.name); // "Hamim"
console.log(student.age);  // 22


// Dot notation is a way to access or set properties and methods of objects in JavaScript


// Bracket Notation
console.log(student["name"]);        // "Hamim"
console.log(student["age"]);         // 22
console.log(student["student id"]);  // "CSE-101"
